class GapDetector:
    """
    Detects memory poisoning attacks that current patterns miss.

    Scans content against known gap signatures and proposes
    PatternCandidates for human review.
    """

    def __init__(self) -> None:
        # Compile all gap signatures
        self._compiled: List[Tuple[Dict, List[re.Pattern]]] = []
        for sig in _GAP_SIGNATURES:
            compiled_pats = []
            for p in sig.get("patterns", []):
                try:
                    compiled_pats.append(re.compile(p, re.IGNORECASE | re.MULTILINE))
                except re.error:
                    pass
            self._compiled.append((sig, compiled_pats))

    def check(self, content: str) -> List[Dict[str, Any]]:
        """
        Check content against gap signatures.

        Returns list of matched signatures (not yet in patterns.py).
        """
        content_lower = content.lower()
        hits = []

        for sig, compiled_pats in self._compiled:
            # Keyword check
            kw_hit = any(kw.lower() in content_lower for kw in sig.get("keywords", []))
            # Pattern check
            pat_hit = any(p.search(content) for p in compiled_pats)
            if kw_hit or pat_hit:
                hits.append({
                    "name": sig["name"],
                    "category": sig["category"],
                    "severity": sig["severity"],
                    "matched_keywords": [kw for kw in sig.get("keywords", []) if kw.lower() in content_lower],
                    "matched_patterns": [p.pattern[:60] for p in compiled_pats if p.search(content)],
                })
        return hits

    def analyze_batch(
        self,
        contents: List[str],
        propose: bool = True,
    ) -> Tuple[List[PatternCandidate], Dict[str, int]]:
        """
        Analyze a batch of content, aggregate gaps, propose candidates.

        Args:
            contents: List of memory content strings
            propose:  If True, create PatternCandidates for new gaps

        Returns:
            (candidates, hit_counts)
        """
        hit_counts: Dict[str, int] = {}
        examples: Dict[str, List[str]] = {}

        for content in contents:
            hits = self.check(content)
            for hit in hits:
                name = hit["name"]
                hit_counts[name] = hit_counts.get(name, 0) + 1
                if name not in examples:
                    examples[name] = []
                if len(examples[name]) < 5:
                    examples[name].append(content[:200])

        candidates = []
        if propose:
            for sig, _ in self._compiled:
                name = sig["name"]
                if name in hit_counts:
                    c = PatternCandidate(
                        candidate_id  = "LEARN-" + _short_id(),
                        name          = name,
                        description   = sig["description"],
                        category      = sig["category"],
                        severity      = sig["severity"],
                        keywords      = sig.get("keywords", []),
                        patterns      = sig.get("patterns", []),
                        examples      = examples.get(name, []),
                        source        = PatternSource.GAP_DETECTION,
                        proposed_at   = _now(),
                        proposed_by   = "gap_detector",
                        hit_count     = hit_counts[name],
                        mitre_id      = sig.get("mitre_id"),
                        cve_id        = sig.get("cve_id"),
                    )
                    candidates.append(c)

        return candidates, hit_counts


