class ReviewDecision(str, Enum):
    PENDING  = "pending"
    APPROVED = "approved"
    REJECTED = "rejected"
    MODIFIED = "modified"


class PatternSource(str, Enum):
    GAP_DETECTION   = "gap_detection"    # missed in production
    FALSE_POSITIVE  = "false_positive"   # over-blocked
    MANUAL          = "manual"           # human authored
    THREAT_INTEL    = "threat_intel"     # from external feed
    CVE             = "cve"              # from published CVE


@dataclass
class PatternCandidate:
    """
    A proposed new or modified pattern awaiting human review.

    Candidates are generated by the GapDetector or manually created.
    None become active until a human explicitly approves them.
    """
    candidate_id:    str
    name:            str
    description:     str
    category:        str          # e.g. "MANIPULATION", "INJECTION"
    severity:        str          # low / medium / high / critical
    keywords:        List[str]
    patterns:        List[str]    # regex patterns
    examples:        List[str]    # real examples that triggered creation
    source:          PatternSource
    proposed_at:     str          # ISO timestamp
    proposed_by:     str          # "gap_detector", "user", etc.
    review:          ReviewDecision = ReviewDecision.PENDING
    reviewed_at:     Optional[str] = None
    reviewed_by:     Optional[str] = None
    reject_reason:   Optional[str] = None
    false_positive_rate: float = 0.0
    hit_count:       int = 0       # times seen in production
    mitre_id:        Optional[str] = None
    cve_id:          Optional[str] = None

    @property
    def is_pending(self) -> bool:
        return self.review == ReviewDecision.PENDING

    @property
    def is_approved(self) -> bool:
        return self.review == ReviewDecision.APPROVED

    def to_dict(self) -> Dict[str, Any]:
        return {
            "candidate_id":       self.candidate_id,
            "name":               self.name,
            "description":        self.description,
            "category":           self.category,
            "severity":           self.severity,
            "keywords":           self.keywords,
            "patterns":           self.patterns,
            "examples":           [e[:200] for e in self.examples[:5]],
            "source":             self.source.value,
            "proposed_at":        self.proposed_at,
            "proposed_by":        self.proposed_by,
            "review":             self.review.value,
            "reviewed_at":        self.reviewed_at,
            "reviewed_by":        self.reviewed_by,
            "reject_reason":      self.reject_reason,
            "false_positive_rate": self.false_positive_rate,
            "hit_count":          self.hit_count,
            "mitre_id":           self.mitre_id,
            "cve_id":             self.cve_id,
        }

    @classmethod
    def from_dict(cls, d: Dict[str, Any]) -> "PatternCandidate":
        return cls(
            candidate_id        = d["candidate_id"],
            name                = d["name"],
            description         = d["description"],
            category            = d.get("category", "UNKNOWN"),
            severity            = d.get("severity", "medium"),
            keywords            = d.get("keywords", []),
            patterns            = d.get("patterns", []),
            examples            = d.get("examples", []),
            source              = PatternSource(d.get("source", "manual")),
            proposed_at         = d.get("proposed_at", _now()),
            proposed_by         = d.get("proposed_by", "unknown"),
            review              = ReviewDecision(d.get("review", "pending")),
            reviewed_at         = d.get("reviewed_at"),
            reviewed_by         = d.get("reviewed_by"),
            reject_reason       = d.get("reject_reason"),
            false_positive_rate = d.get("false_positive_rate", 0.0),
            hit_count           = d.get("hit_count", 0),
            mitre_id            = d.get("mitre_id"),
            cve_id              = d.get("cve_id"),
        )


@dataclass
class FalsePositive:
    """A reported false positive — safe content that was incorrectly blocked."""
    fp_id:        str
    content:      str
    threat_id:    str           # which pattern falsely matched
    reported_at:  str
    reported_by:  str
    context:      str = ""
    resolved:     bool = False
    whitelist_added: bool = False

    def to_dict(self) -> Dict[str, Any]:
        return {
            "fp_id": self.fp_id,
            "content": self.content[:300],
            "threat_id": self.threat_id,
            "reported_at": self.reported_at,
            "reported_by": self.reported_by,
            "context": self.context,
            "resolved": self.resolved,
            "whitelist_added": self.whitelist_added,
        }


