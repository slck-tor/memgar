# Pattern Store (hash-chained, tamper-evident)
# ---------------------------------------------------------------------------

class PatternStore:
    """
    Signed, hash-chained storage for learned patterns.

    Every write is chained to the previous entry (same as MemoryLedger)
    so any tampering — including adversarial pattern injection —
    is immediately detectable.
    """

    GENESIS = "0" * 64

    def __init__(self, path: Optional[str] = None) -> None:
        self._path = Path(path) if path else None
        self._candidates: Dict[str, PatternCandidate] = {}
        self._false_positives: List[FalsePositive] = []
        self._chain: List[Dict] = []  # hash chain log
        self._load()

    # ── Public API ──────────────────────────────────────────────────────────

    def add_candidate(self, candidate: PatternCandidate) -> None:
        self._candidates[candidate.candidate_id] = candidate
        self._append_chain("add_candidate", candidate.candidate_id,
                           candidate.name)
        self._save()

    def update_candidate(self, candidate: PatternCandidate) -> None:
        self._candidates[candidate.candidate_id] = candidate
        self._append_chain("update_candidate", candidate.candidate_id,
                           candidate.review.value)
        self._save()

    def add_false_positive(self, fp: FalsePositive) -> None:
        self._false_positives.append(fp)
        self._append_chain("add_fp", fp.fp_id, fp.threat_id)
        self._save()

    def get(self, candidate_id: str) -> Optional[PatternCandidate]:
        return self._candidates.get(candidate_id)

    def all_candidates(self) -> List[PatternCandidate]:
        return list(self._candidates.values())

    def pending(self) -> List[PatternCandidate]:
        return [c for c in self._candidates.values() if c.is_pending]

    def approved(self) -> List[PatternCandidate]:
        return [c for c in self._candidates.values() if c.is_approved]

    def false_positives(self) -> List[FalsePositive]:
        return list(self._false_positives)

    def verify_chain(self) -> Tuple[bool, int]:
        """Verify integrity of the learning store. Returns (is_valid, error_count)."""
        prev_hash = self.GENESIS
        errors = 0
        for entry in self._chain:
            computed = hashlib.sha256(
                f"{entry['seq']}|{entry['op']}|{entry['ref']}|{entry['ts']}|{prev_hash}".encode()
            ).hexdigest()
            if computed != entry.get("hash", ""):
                errors += 1
            prev_hash = entry.get("hash", prev_hash)
        return errors == 0, errors

    # ── Internal ────────────────────────────────────────────────────────────

    def _append_chain(self, op: str, ref: str, detail: str) -> None:
        prev_hash = self._chain[-1]["hash"] if self._chain else self.GENESIS
        seq = len(self._chain)
        ts = _now()
        h = hashlib.sha256(
            f"{seq}|{op}|{ref}|{ts}|{prev_hash}".encode()
        ).hexdigest()
        self._chain.append({
            "seq": seq, "op": op, "ref": ref,
            "detail": detail[:80], "ts": ts, "hash": h,
        })

    def _load(self) -> None:
        if not self._path or not self._path.exists():
            return
        try:
            data = json.loads(self._path.read_text(encoding="utf-8"))
            for d in data.get("candidates", []):
                c = PatternCandidate.from_dict(d)
                self._candidates[c.candidate_id] = c
            for d in data.get("false_positives", []):
                self._false_positives.append(FalsePositive(**d))
            self._chain = data.get("chain", [])
        except Exception:
            pass

    def _save(self) -> None:
        if not self._path:
            return
        self._path.parent.mkdir(parents=True, exist_ok=True)
        data = {
            "version": "1.0",
            "saved_at": _now(),
            "candidates": [c.to_dict() for c in self._candidates.values()],
            "false_positives": [fp.to_dict() for fp in self._false_positives],
            "chain": self._chain,
        }
        self._path.write_text(
            json.dumps(data, indent=2, ensure_ascii=False),
            encoding="utf-8"
        )


