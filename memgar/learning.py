def scan_for_gaps(contents: List[str]) -> List[Dict[str, Any]]:
    """
    Quick scan — returns gap matches without creating candidates.

    Useful for one-off checks without a persistent store.
    """
    detector = GapDetector()
    results = []
    for content in contents:
        hits = detector.check(content)
        if hits:
            results.append({"content_preview": content[:100], "gaps": hits})
    return results


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _now() -> str:
    return datetime.now(tz=timezone.utc).isoformat()


def _short_id() -> str:
    return uuid.uuid4().hex[:8].upper()
