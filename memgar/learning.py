"""
Memgar Self-Learning Pattern System
=====================================

Human-supervised, tamper-proof pattern learning engine.

Architecture:
    PatternLearner       — core learning engine
    PatternCandidate     — proposed new pattern awaiting review
    PatternReview        — human decision on a candidate
    PatternStore         — signed, versioned pattern storage
    FalsePositiveTracker — tracks over-blocking for whitelist tuning
    GapDetector          — finds missed attacks in production logs

Learning pipeline (safe by design):

    Production traffic
          │
          ▼
    GapDetector.analyze()
          │
          ▼
    PatternCandidate created  ← human-readable, auditable
          │
          ▼
    PatternLearner.review(candidate, decision=APPROVE)
          │                 (or REJECT, or MODIFY)
          ▼
    PatternStore.commit()  ← signed with SHA-256 Merkle chain
          │
          ▼
    Analyzer.add_runtime_pattern()  ← takes effect immediately
          │
          ▼  (periodically)
    export_to_patterns_py()  ← permanent PR to patterns.py

Why this is safe:
    • No pattern takes effect without explicit human APPROVE
    • Every pattern has provenance (who proposed, who approved, when)
    • PatternStore is hash-chained (tamper detection like MemoryLedger)
    • Adversarial inputs cannot auto-create patterns (gap must be
      detected AND approved)
    • Patterns can be revoked at any time

CLI usage:
    memgar learn scan ./logs/          # detect gaps
    memgar learn review                # review pending candidates
    memgar learn approve <id>          # approve a candidate
    memgar learn reject <id> [reason]  # reject a candidate
    memgar learn status                # show learning stats
    memgar learn export                # export approved to patterns.py

Python usage:
    from memgar.learning import PatternLearner

    learner = PatternLearner(store_path="./memgar_learned.json")

    # Analyze production traffic for gaps
    gaps = learner.detect_gaps(["content1", "content2", ...])

    # Review and approve
    for candidate in learner.pending():
        learner.approve(candidate.id, reviewer="security-team")

    # Apply approved patterns to runtime
    learner.apply(analyzer)
"""

