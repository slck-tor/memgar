class PatternLearner:
    """
    Human-supervised pattern learning engine.

    Manages the full lifecycle:
    1. Gap detection (what are we missing?)
    2. Candidate proposal (what should we add?)
    3. Human review (approve / reject / modify)
    4. Runtime application (patterns take effect immediately)
    5. Export (permanent addition to patterns.py)

    Args:
        store_path:      Path to persist learning state (.json)
        on_new_candidate: Callback when a new candidate is proposed
        auto_approve_cve: Auto-approve patterns linked to published CVEs
                          (still logged, still reversible, but no manual step)
    """

    def __init__(
        self,
        store_path: Optional[str] = None,
        on_new_candidate: Optional[Callable[[PatternCandidate], None]] = None,
        auto_approve_cve: bool = False,
    ) -> None:
        self._store = PatternStore(path=store_path)
        self._on_new = on_new_candidate
        self._auto_approve_cve = auto_approve_cve
        self._gap_detector = GapDetector()
        self._runtime_patterns: List[Any] = []  # Threat objects

    # ── Gap detection ───────────────────────────────────────────────────────

    def detect_gaps(self, contents: List[str]) -> List[PatternCandidate]:
        """
        Scan content for attacks the current patterns miss.

        Returns new PatternCandidates (added to store as PENDING).
        """
        candidates, counts = self._gap_detector.analyze_batch(contents, propose=True)
        new = []
        existing_names = {c.name for c in self._store.all_candidates()}

        for candidate in candidates:
            if candidate.name in existing_names:
                # Update hit count on existing candidate
                existing = next(
                    (c for c in self._store.all_candidates() if c.name == candidate.name), None
                )
                if existing:
                    existing.hit_count += candidate.hit_count
                    existing.examples = list(set(existing.examples + candidate.examples))[:5]
                    self._store.update_candidate(existing)
                continue

            # Auto-approve CVE-backed patterns if configured
            if self._auto_approve_cve and candidate.cve_id:
                candidate.review = ReviewDecision.APPROVED
                candidate.reviewed_at = _now()
                candidate.reviewed_by = "auto_cve"

            self._store.add_candidate(candidate)
            new.append(candidate)

            if self._on_new:
                try:
                    self._on_new(candidate)
                except Exception:
                    pass

        return new

    def check_content(self, content: str) -> List[Dict[str, Any]]:
        """
        Check a single content against gap signatures.
        Does NOT create candidates — just returns matches.
        """
        return self._gap_detector.check(content)

    # ── Review ──────────────────────────────────────────────────────────────

    def approve(
        self,
        candidate_id: str,
        reviewer: str = "human",
        notes: str = "",
    ) -> Optional[PatternCandidate]:
        """Approve a candidate pattern. It will be applied on next apply() call."""
        candidate = self._store.get(candidate_id)
        if not candidate:
            return None
        candidate.review = ReviewDecision.APPROVED
        candidate.reviewed_at = _now()
        candidate.reviewed_by = reviewer
        if notes:
            candidate.reject_reason = notes  # reuse field for notes
        self._store.update_candidate(candidate)
        return candidate

    def reject(
        self,
        candidate_id: str,
        reason: str,
        reviewer: str = "human",
    ) -> Optional[PatternCandidate]:
        """Reject a candidate pattern."""
        candidate = self._store.get(candidate_id)
        if not candidate:
            return None
        candidate.review = ReviewDecision.REJECTED
        candidate.reviewed_at = _now()
        candidate.reviewed_by = reviewer
        candidate.reject_reason = reason
        self._store.update_candidate(candidate)
        return candidate

    def modify(
        self,
        candidate_id: str,
        keywords: Optional[List[str]] = None,
        patterns: Optional[List[str]] = None,
        severity: Optional[str] = None,
        reviewer: str = "human",
    ) -> Optional[PatternCandidate]:
        """Modify a candidate before approving."""
        candidate = self._store.get(candidate_id)
        if not candidate:
            return None
        if keywords is not None:
            candidate.keywords = keywords
        if patterns is not None:
            candidate.patterns = patterns
        if severity is not None:
            candidate.severity = severity
        candidate.review = ReviewDecision.MODIFIED
        candidate.reviewed_at = _now()
        candidate.reviewed_by = reviewer
        self._store.update_candidate(candidate)
        return candidate

    # ── False positive tracking ──────────────────────────────────────────────

    def report_false_positive(
        self,
        content: str,
        threat_id: str,
        reported_by: str = "user",
        context: str = "",
    ) -> FalsePositive:
        """
        Report a false positive (safe content that was blocked).

        Aggregates into whitelist candidates for human review.
        """
        fp = FalsePositive(
            fp_id       = "FP-" + _short_id(),
            content     = content[:500],
            threat_id   = threat_id,
            reported_at = _now(),
            reported_by = reported_by,
            context     = context,
        )
        self._store.add_false_positive(fp)
        return fp

    # ── Apply approved patterns ──────────────────────────────────────────────

    def apply(self, analyzer: Any) -> int:
        """
        Apply all approved patterns to a running Analyzer instance.

        Converts approved PatternCandidates to Threat objects and
        adds them to the analyzer's pattern list.

        Returns count of patterns applied.
        """
        from memgar.models import Threat, ThreatCategory, Severity

        already_applied = {
            getattr(p, "id", "") for p in getattr(analyzer, "patterns", [])
        }
        applied = 0

        for candidate in self._store.approved():
            cid = "LEARN-" + candidate.candidate_id[-8:]
            if cid in already_applied:
                continue

            # Map severity
            sev_map = {
                "low": Severity.LOW, "medium": Severity.MEDIUM,
                "high": Severity.HIGH, "critical": Severity.CRITICAL,
            }
            sev = sev_map.get(candidate.severity.lower(), Severity.HIGH)

            # Map category
            cat_map = {
                "MANIPULATION": ThreatCategory.MANIPULATION,
                "INJECTION":    ThreatCategory.INJECTION,
                "EXFILTRATION": ThreatCategory.EXFILTRATION,
                "EVASION":      ThreatCategory.EVASION,
                "SLEEPER":      ThreatCategory.SLEEPER,
                "PRIVILEGE":    ThreatCategory.PRIVILEGE,
                "SOCIAL":       ThreatCategory.SOCIAL,
            }
            cat = cat_map.get(candidate.category.upper(), ThreatCategory.INJECTION)

            threat = Threat(
                id          = cid,
                name        = candidate.name,
                description = candidate.description,
                category    = cat,
                severity    = sev,
                patterns    = candidate.patterns,
                keywords    = candidate.keywords,
                examples    = candidate.examples,
                mitre_attack= candidate.mitre_id or "",
            )

            if not hasattr(analyzer, "patterns"):
                analyzer.patterns = []
            analyzer.patterns.append(threat)

            # Also register keyword in cache
            from memgar.analyzer import _get_keyword_pattern
            for kw in candidate.keywords:
                _get_keyword_pattern(kw)

            applied += 1

        return applied

    # ── Export to patterns.py ────────────────────────────────────────────────

    def export_to_python(self, output_path: Optional[str] = None) -> str:
        """
        Export approved patterns as Python code for patterns.py.

        Returns the generated code string. If output_path is given,
        also writes to that file.
        """
        approved = self._store.approved()
        if not approved:
            return "# No approved patterns to export\n"

        lines = [
            "# =============================================================================",
            "# LEARNED PATTERNS — Human-approved via PatternLearner",
            f"# Generated: {_now()}",
            f"# Count: {len(approved)}",
            "# =============================================================================",
            "",
        ]

        for c in approved:
            var_name = "LEARN_" + re.sub(r"[^A-Z0-9]", "_", c.name.upper())
            sev = c.severity.upper()
            cat = c.category.upper()

            kw_lines = ",\n        ".join(f'"{kw}"' for kw in c.keywords)
            pat_lines = ",\n        ".join(f'r"{p}"' for p in c.patterns)
            ex_lines  = ",\n        ".join(f'"{e[:100]}"' for e in c.examples[:3])

            lines += [
                f"{var_name} = Threat(",
                f'    id="{c.candidate_id[-8:].upper()}",',
                f'    name="{c.name}",',
                f'    description="{c.description}",',
                f"    category=ThreatCategory.{cat},",
                f"    severity=Severity.{sev},",
                f"    keywords=[",
                f"        {kw_lines}",
                f"    ],",
                f"    patterns=[",
                f"        {pat_lines}",
                f"    ],",
                f"    examples=[",
                f"        {ex_lines}",
                f"    ],",
                f'    mitre_attack="{c.mitre_id or ""}",',
                f")",
                "",
            ]

        # Add to PATTERNS list
        var_names = ["LEARN_" + re.sub(r"[^A-Z0-9]", "_", c.name.upper()) for c in approved]
        lines += [
            "PATTERNS.extend([",
            "    " + ",\n    ".join(var_names),
            "])",
            "",
        ]

        code = "\n".join(lines)

        if output_path:
            Path(output_path).write_text(code, encoding="utf-8")

        return code

    # ── Status & introspection ───────────────────────────────────────────────

    def pending(self) -> List[PatternCandidate]:
        return self._store.pending()

    def stats(self) -> LearningStats:
        all_c = self._store.all_candidates()
        return LearningStats(
            candidates_proposed = len(all_c),
            candidates_approved = sum(1 for c in all_c if c.is_approved),
            candidates_rejected = sum(1 for c in all_c if c.review == ReviewDecision.REJECTED),
            candidates_pending  = sum(1 for c in all_c if c.is_pending),
            false_positives     = len(self._store.false_positives()),
            patterns_applied    = len(self._runtime_patterns),
            gaps_detected       = sum(c.hit_count for c in all_c),
        )

    def verify_store(self) -> Tuple[bool, int]:
        """Verify tamper-evidence of the learning store."""
        return self._store.verify_chain()

    def all_candidates(self) -> List[PatternCandidate]:
        return self._store.all_candidates()


